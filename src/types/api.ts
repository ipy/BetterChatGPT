export interface EventSourceDataInterface {
  choices: EventSourceDataChoices[];
  created: number;
  id: string;
  model: string;
  object: string;
}

export type EventSourceData = EventSourceDataInterface | '[DONE]';

export interface EventSourceDataMessage {
  content?: string;
  role?: string;
}

export interface EventSourceDataChoices {
  delta?: EventSourceDataMessage; // stream mode
  message?: EventSourceDataMessage; // non-stream mode
  finish_reason?: string;
  index: number;
}

export interface ShareGPTSubmitBodyInterface {
  avatarUrl: string;
  items: {
    from: 'gpt' | 'human';
    value: string;
  }[];
}
